package modelo;

/**
 *
 * @author Idalia
 */
public class Cuadrado {
    
    private String nombre;
    private double lado;

    public Cuadrado() {
    }
    
    public Cuadrado(String nombre, double lado) {
        this.nombre = nombre;
        this.lado = lado;
    }

    public double getLado() {
        return lado;
    }

    public void setLado(double lado) {
        this.lado = lado;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    
    
}
